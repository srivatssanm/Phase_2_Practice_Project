
public class ProductDetails {
	
		private int id;
		private String name;
		private int price;
		public int getPrice() {
			return price;
		}
		public void setPrice(int price) {
			this.price = price;
		}
		public int getid() {
			return id;
		}
		public void setid(int id) {
			this.id=id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		
	}
